CREATE TABLE PATIENTS (
    Id UUID NOT NULL PRIMARY KEY,
    BIRTHDATE DATE NOT NULL CHECK (BIRTHDATE <= CURRENT_DATE),
    DEATHDATE DATE CHECK (DEATHDATE <= CURRENT_DATE),
    SSN VARCHAR(11) NOT NULL,
    DRIVERS VARCHAR(9),
    PASSPORT VARCHAR(10),
    PREFIX VARCHAR(4),
    FIRST VARCHAR(30) NOT NULL,
    LAST VARCHAR(30) NOT NULL,
    SUFFIX VARCHAR(5),
    MAIDEN VARCHAR(30),
    MARTIAL CHAR(1),
    RACE VARCHAR(10) NOT NULL ,
    ETHNICITY VARCHAR(20) NOT NULL ,
    GENDER CHAR(1) CHECK (GENDER IN ('M', 'F')),
    BIRTHPLACE VARCHAR(50),
    ADDRESS VARCHAR(100),
    CITY VARCHAR(50),
    STATE VARCHAR(30),
    COUNTY VARCHAR(30),
    ZIP VARCHAR(5),
    LAT DECIMAL(10,8),
    LON DECIMAL(11,8),
    HEALTHCARE_EXPENSES DECIMAL(15,4),
    HEALTHCARE_COVERAGE DECIMAL(15,4)
);

CREATE TABLE PAYERS (
     Id UUID NOT NULL PRIMARY KEY,
     NAME VARCHAR(50) NOT NULL,
     ADDRESS VARCHAR(100) NOT NULL,
     CITY VARCHAR(50) NOT NULL,
     STATE_HEADQUARTERED VARCHAR(5) NOT NULL,
     ZIP VARCHAR(5),
     PHONE VARCHAR(14),
     AMOUNT_COVERED NUMERIC(15,2) NOT NULL,
     AMOUNT_UNCOVERED NUMERIC(15,2) NOT NULL,
     REVENUE NUMERIC(15,2) NOT NULL,
     COVERED_ENCOUNTERS INT NOT NULL,
     UNCOVERED_ENCOUNTERS INT NOT NULL,
     COVERED_MEDICATIONS INT NOT NULL,
     UNCOVERED_MEDICATIONS INT NOT NULL,
     COVERED_PROCEDURES INT NOT NULL,
     UNCOVERED_PROCEDURES INT NOT NULL,
     COVERED_IMMUNIZATIONS INT NOT NULL,
     UNCOVERED_IMMUNIZATIONS INT NOT NULL,
     UNIQUE_CUSTOMERS INT NOT NULL,
     QOLS_AVG NUMERIC(15,14) NOT NULL,
     MEMBER_MONTHS INT NOT NULL
);

CREATE TABLE PAYER_TRANSITIONS (
    PATIENT UUID NOT NULL,
    MEMBERID UUID NOT NULL,
    START_YEAR TIMESTAMPTZ NOT NULL,
    END_YEAR TIMESTAMPTZ NOT NULL,
    PAYER UUID NOT NULL,
    SECONDARY_PAYER UUID,
    OWNERSHIP VARCHAR(10) NOT NULL,
    OWNERNAME VARCHAR(100) NOT NULL,
    PRIMARY KEY (PATIENT, MEMBERID, START_YEAR)
);